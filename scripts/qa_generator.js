function generateQA(){
    var num = 1;
	var currentChapter = "first";
	$("#qa_body").append("<div class=\"accordion\" id=\"accordionQA\">");
    questions.forEach(element => {
		if (element.chapter != currentChapter)
		{
			$("#qa_body").append("<h5>" + element.chapter + "</h5>");
			currentChapter = element.chapter;
		}
        toAppendString = "<div class=\"accordion-item\"><h2 class=\"accordion-header\" id=\"heading" + num + "\">"
		+ "<button class=\"accordion-button collapsed\" type=\"button\" data-bs-toggle=\"collapse\" data-bs-target=\"#collapse" + num + "\" aria-expanded=\"false\" aria-controls=\"collapse"
		+ num + "\">" + element.question + "</button></h2><div id=\"collapse" + num + "\" class=\"accordion-collapse collapse\" aria-labelledby=\"heading" + num + "\" data-bs-parent=\"#accordionQA\">"
		+ "<div class=\"accordion-body\"><p>" + element.answer + "</p></div></div></div>";
        $("#qa_body").append(toAppendString);
        num++;
      });

}